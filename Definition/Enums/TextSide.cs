namespace Wuest.FlatFileParser.Definition.Enums
{
	/// <summary>
	/// This enum determines which side of text to use for various operations.
	/// </summary>
	public enum TextSide : byte
	{
		/// <summary>
		/// The operation is done on the left-hand side of the text.
		/// </summary>
		Left = 0,
		
		/// <summary>
		/// The operation is done on the right-hand side of the text.
		/// </summary>
		Right
	}
}
